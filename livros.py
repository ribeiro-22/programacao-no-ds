# Ordenando uma lista de títulos de livros
titulos_livros = ["O Apanhador no Campo de Centeio", "1984", "Orgulho e Preconceito", "Dom Quixote"]
titulos_livros.sort()
print("Títulos ordenados:", titulos_livros)


# Ordenando uma lista de sobrenomes de autores
autores = ["Orwell", "Salinger", "Austen", "Cervantes"]
autores.sort()
print("Autores ordenados:", autores)


# Ordenando uma lista de datas de publicação
datas_publicacao = [1951, 1949, 1813, 1605]
datas_publicacao_ordenadas = sorted(datas_publicacao)
print("Datas de publicação ordenadas:", datas_publicacao_ordenadas)
